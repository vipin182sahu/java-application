
class CopyrightModel {

	public String holder;
	public String licence;
	public String holder_url;
	public int year;
	public String licence_url;
	public String logo;

	public CopyrightModel(String holder, String licence, String holder_url, int year, String licence_url, String logo) {

		this.holder = holder;
		this.licence = licence;
		this.holder_url = holder_url;
		this.year = year;
		this.licence_url = licence_url;
		this.logo = logo;

	}

}