
class CopyrightModel {

	public int year;
	public String licence_url;
	public String holder;
	public String logo;
	public String holder_url;
	public String licence;

	public CopyrightModel(int year, String licence_url, String holder, String logo, String holder_url, String licence) {

		this.year = year;
		this.licence_url = licence_url;
		this.holder = holder;
		this.logo = logo;
		this.holder_url = holder_url;
		this.licence = licence;

	}

}