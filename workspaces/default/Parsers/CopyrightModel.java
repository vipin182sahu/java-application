
class CopyrightModel {

	public int year;
	public String logo;
	public String holder;
	public String licence;
	public String holder_url;
	public String licence_url;

	public CopyrightModel(int year, String logo, String holder, String licence, String holder_url, String licence_url) {

		this.year = year;
		this.logo = logo;
		this.holder = holder;
		this.licence = licence;
		this.holder_url = holder_url;
		this.licence_url = licence_url;

	}

}